        public int ComputeFlag(int id, double[] values, double isovalue)
        {
            int flag = 0;
            for (int i = 0; i < 8; i++)
            {
                // check the state of each vertice.
                if (values[i] > isovalue)
                {
                    flag |= 1 << i;
                }
            }
            Cases[id] = flag;
            return flag;
        }
        public List<Vector3d> ExtractIsosurface(Vector3d[] voxelCorners, double[] values, double isovalue, Vector3d voxelSize, bool interpolation)
        {
            List<Vector3d> pts = new List<Vector3d>();
            int flag = ComputeFlag(0, values, isovalue);
            // To find edges which intersect with the boundary
            int EdgeFlag = CubeEdgeFlags[flag];


            // This voxel is in the boundary.
            if (EdgeFlag == 0) return new List<Vector3d>();

            Vector3d[] EdgeVertex = new Vector3d[12];
            for (int i = 0; i < 12; i++)
            {
                if ((EdgeFlag & (1 << i)) != 0)
                {
                    var Offset = GetOffset(values[EdgeConnection[i, 0]], values[EdgeConnection[i, 1]], isovalue, interpolation);
                    EdgeVertex[i] = new Vector3d(
                    voxelCorners[0].X + (Vertices[EdgeConnection[i, 0], 0] + Offset * EdgeDirection[i, 0]) * voxelSize.X,
                    voxelCorners[0].Y + (Vertices[EdgeConnection[i, 0], 1] + Offset * EdgeDirection[i, 1]) * voxelSize.Y,
                    voxelCorners[0].Z + (Vertices[EdgeConnection[i, 0], 2] + Offset * EdgeDirection[i, 2]) * voxelSize.Z);
                }
            }


            // Generate triangles
            for (int Triangle = 0; Triangle < 5; Triangle++)
            {
                if (TriangleConnectionTable[flag, 3 * Triangle] < 0)
                    break;


                for (int Corner = 0; Corner < 3; Corner++)
                {
                    int Vertex = TriangleConnectionTable[flag, 3 * Triangle + Corner];
                    Vector3d pd = new Vector3d(EdgeVertex[Vertex].X, EdgeVertex[Vertex].Y, EdgeVertex[Vertex].Z);
                    pts.Add(pd);
                }
            }
            return pts;
        }

        /// <summary>
        /// To compute interpolated points.
        /// </summary>
        /// <param name="value1"> The first value. </param>
        /// <param name="value2"> The second value. </param>
        /// <param name="isovalue"> The isovalue. </param>
        /// <param name="interpolation"> Whether to use linear interpolation. </param>
        /// <returns></returns>
        private double GetOffset(double value1, double value2, double isovalue, bool interpolation)
        {
            if (!interpolation)
                return 0.5;

            if (Math.Abs(isovalue - value1) < 1e-12)
            {
                return 0;
            }

            return (isovalue - value1) / (value2 - value1);
        }

